lli mul(lli a, lli b) {
  return (a%MOD*b%MOD)%MOD;
}

lli power(lli a,lli b) {
  lli ans = 1;
  while(b > 0) {
    if(b&1)
      ans = mul(ans, a);
    a = mul(a,a);;
    b >>= 1;
  }
  return ans;
}

void solve() {
  lli n;
  vector<lli> arr;
  cin >> n;
  input(arr, 2*n);
  vector<lli> odd;
  vector<lli> even;
  int i=1;
  for(auto x:arr) {
    if(x%2)
      odd.pb(i);
    else
      even.pb(i);
    ++i;
  }

  if(odd.size()%2!=0 or even.size()%2!=0) {
    odd.pop_back();
    even.pop_back();
  } else {
    if(odd.size()>=2)
      odd.pop_back(), odd.pop_back();
    else
      even.pop_back(), even.pop_back();
  }

  for(int i=0; i<int(odd.size())-1; i+=2)
    cout << odd[i] << " " << odd[i+1] << endl;

  for(int i=0; i<int(even.size())-1; i+=2)
    cout << even[i] << " " << even[i+1] << endl;
}

