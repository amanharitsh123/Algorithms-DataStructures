lli mul(lli a, lli b) {
  return (a%MOD*b%MOD)%MOD;
}

lli power(lli a,lli b) {
  lli ans = 1;
  while(b > 0) {
    if(b&1)
      ans = mul(ans, a);
    a = mul(a,a);;
    b >>= 1;
  }
  return ans;
}

void solve() {
  int n, temp;
  cin >> n;
  map<int, vector<int> > a;
  for(int i=0; i<n; i++) {
    cin >> temp;
    a[temp] = {i, n+i};
  }

  map<int, int> ans;
  for(int i=0; i<n; i++) {
    cin >> temp;
    int x = i-a[temp][0];
    int y = i-a[temp][1];
    if(x==y) 
      ans[x]++;
    else
      ans[x]++, ans[y]++;
  }

  int final_ans = 0;
  for(int i=-n; i<=n; i++)
    final_ans = max(final_ans, ans[i]);

  cout << final_ans << endl;
}

